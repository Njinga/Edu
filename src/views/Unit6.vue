                                <h4 id="item-3-4-1" class="h4-responsive">6.3.4.1 Maintenance of computer equipment </h4>
                                <div class="ml-5">

                                    <p class="text-justify meme">
                                        Computers and related technologies are subjected to the common problems of
                                        any equipment such as; regular breakdown, malfunction, damage or being stolen.
                                        We will mention some ongoing maintenance and security concerns that could assist
                                        in the maintenance of your equipment.
                                    </p>
                                    <p class="text-justify meme">
                                        <ol>
                                            <li>
                                                <i>Laboratory rules. </i> Most laboratory rules are
                                                intended to extend the lives of the resources that you buy.
                                                The lab rules and regulations should be posted prominently
                                                and should apply to everyone who uses the lab  Such rules should
                                                cover areas like:
                                                <ul>
                                                    <li>
                                                        No eating, drinking, or smoking should be allowed near equipment.
                                                    </li>
                                                    <li>Lab resources should be reserved for instructional purposes only.</li>
                                                    <li>Only authorized lab personnel should check out lab resources.</li>
                                                    <li>Schedules for use should be strictly observed.</li>
                                                    <li>Problems with equipment should be reported promptly to designated personnel.</li>
                                                    <li>Lab users should show respect for others by maintaining appropriate noise levels.</li>
                                                </ul><br /><br />
                                            </li>
                                            <li>
                                                <i>Maintenance needs and options. </i> Each teacher who uses the equipment needs training
                                                in simple troubleshooting procedures such as making sure a printer is plugged in and on-line or
                                                what to do when the computer display a message "a disk is unreadable".  Schools could minimize
                                                repair problems if users follow good usage rules and conduct preventive maintenance procedures.
                                                For the computer and other equipment, it is necessary to have a maintenance policy. For example,
                                                establish a maintenance contract with an external supplier and set aside a maintenance budget.<br /><br />
                                            </li>

                                            <li>
                                                <i>Securing the resources</i> Security is a separate but equally important maintenance issue
                                                since loss of equipment from vandalism and theft is a common problem in schools.
                                                Many of the computer accessories are very portable and can easily be stolen. A school
                                                needs to devise a security system that would work for them to protect the equipment from
                                                being stolen. <br /><br />
                                            </li>

                                            <li>
                                                <i>Viruses: causes, prevention and cures. </i> Computer viruses are programmes written
                                                specifically to cause damage or corrupt other programmes or information (Hansen & Koltes, 1992).
                                                Like real viruses, these programmes can be passed to other programmes with which they come in contact.
                                                For instance, they infect other computers by connecting one computer to another via telecommunications or by
                                                inserting a disk containing the virus into a computer. Some viruses are carried into a computer system on
                                                <kk><i>Trojan horses</i></kk> or attractive programmes designed for another purpose but which carry
                                                instructions that get around protection codes (Lee, 1992). Some viruses are <i>worms</i> or programmes
                                                designed specifically to run simultaneously with other programmes. Many different strains of viruses
                                                plague computer systems and more are being generated.  The impact of the virus takes many forms. Some
                                                viruses eat through data stored in a computer; others replicate copies of themselves in computer memory and
                                                destroy files. Viruses have the general effect of tying up computer resources, frustrating users and wasting
                                                valuable time.  To deal with the problem of viruses, Dormady (1991) recommends a four-point programme to minimize
                                                the impact of viruses: establish good practices (scan systems and disks regularly, back up important data or files,
                                                enforce safety policies (no unauthorized programmes), use virus management programmes.
                                            </li>
                                        </ol>

                                    </p>
