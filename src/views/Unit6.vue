                                <h4 id="item-3-1-1" class="h4-responsive">6.3.1.1 Operating the Overhead Projector/Computer Projector</h4>
                                <div class="ml-5">
                                    <br />
                                    <p class="text-justify meme">
                                        To get the best out of your overhead or computer projector, you need to know how to
                                        operate it the right way. Follow the following steps to operate an overhead projector:
                                        <ul>
                                            <li>Connect the power cord to the power outlet</li>
                                            <li>Turn the projector on</li>
                                            <li>Position the transparency on stage</li>
                                            <li>Adjust the projector to eliminate key stoning effect</li>
                                            <li>Focus the image.</li>
                                        </ul>
                                    </p>
                                    <p class="text-justify meme">
                                        <i><kk>Key stone effect</kk></i> refers to a distortion of the image.
                                        The effect takes its name from the typical shape of a key-stoned image
                                        which is wide at the top and narrower at the bottom like a keystone.
                                        The key stone is caused by the position in which the projector is placed.
                                        If the projector is too high, too low or off either sides, a distortion of
                                        the image will occur. To remedy this situation, you can either  move the projector
                                        or the screen to bring the two into perpendicular relationship.
                                    </p>
                                    <p class="text-justify meme">
                                        Once you have aligned the projector and screen, consider the distance between them.
                                        If the distance is too long, the image will spill over the edges of a screen and if the
                                        distance is too short, the image will not fill the screen properly.
                                    </p>
                                    <p class="text-justify meme">
                                        The basic rule of thumb is:
                                        <ul>
                                            <li>
                                                If the projected image is too large for the screen, push the projector closer.
                                            </li>

                                            <li>
                                                If the image is too small, pull the projector back.
                                            </li>
                                        </ul>
                                    </p>
                                    <p class="text-justify meme">
                                        <kk><i>Trouble-shooting and remedies with overhead projection</i></kk>
                                        At times you want to use the overhead projector but some problems might arise .
                                        You need to know what to do in such situations. Such problems that might prevent
                                        you from using the equipment are called <kk><i>trouble -shooting spots</i></kk>.For example:

                                        <ul>
                                            <li>
                                                When no power comes on after switching on the equipment.
                                                You need to be sure that the projector is plugged into an electric
                                                outlet and that there is electric power in that power point. At times
                                                the equipment switch might be defective and you might have to replace it.
                                            </li>
                                            <li>
                                                If the lens fails to focus despite all
                                                adjustments of the focus, determine whether the lens is dirty or the focus control is faulty.
                                            </li>
                                            <li>
                                                If the lamp is burned out, light will not show on the projector and you will
                                                need to replace the lamp with another lamp of the same voltage.
                                            </li>
                                        </ul>

                                    </p>
                                    <div class="note note-primary">
