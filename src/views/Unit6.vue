                                <div class="ml-5">
                                    <p class="text-justify meme">
                                        The overhead projector (For more information see section 2.3.5 - Projected Media)
                                        is an equipment with a few components that require special maintenance procedures.
                                        However, you need to take a few basic precautions to ensure that the projector keeps
                                        putting on a bright performance. Some of the precautions include:
                                    </p>
                                    <p class="text-justify meme">
                                        <ul>
                                            <li>
                                                Keeping it as clean as possible. The horizontal stage which is made up of
                                                glass tends to gather dust and finger prints stick on. It should be cleaned
                                                regularly with lens tissue and proper lens cleaning solution. If possible,
                                                keep the glass surface covered in flannel cloth. This will help avoid
                                                scratching and dust accumulation.
                                            </li>
                                        </ul>
                                    </p>
                                    <p class="text-justify meme">
                                        <ul>
                                            <li>
                                                Protecting <kk><i>the projector lamp. The projector lamp</i></kk> which generates light
                                                for projection should be protected in order to lengthen its life span since it is
                                                very expensive. The best way to lengthen the life of the lamp is to ensure that the
                                                projector is not left on for too long since overheating damages the lamp. Never
                                                touch the lamp when it is hot nor should you touch a new lamp with bare fingers
                                                since this shortens its life span.
                                            </li>
                                        </ul>
                                    </p>
                                    <p class="text-justify meme">
                                        <ul>

                                            <li>
                                                Whenever you are not using it, switch off the projector.  Also, after using the projector,
                                                move it to allow time for cooling.
                                            </li>
                                            <li>
                                                Move the projector, with care.
                                            </li>
                                        </ul>
                                    </p>
