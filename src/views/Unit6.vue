                                        Before using the LCD projector make sure that all the required cables are available
                                        and have not been damaged. You may need an extension cable in case where you are projecting
                                        from is far from an electrical socket. Incase you are using the projector in an area where
                                        there is no electricity or electricity is unstable, have a standby generator.
                                    </p>
                                    <p class="text-justify meme">
                                        The projection screen must be clean. In case there is a whiteboard in the classroom you can project on it.
