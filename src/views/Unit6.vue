                                <h3 id="item-3-2" class="h3-responsive">6.3.2 Audio equipment</h3>
                                <div class="ml-5">
                                    <p class="text-justify meme">
                                        Audio equipment and gadgets have evolved from reel to reel tapes, to cassettes, to CD's, to audio files
                                        and now  to podcasts. The equipment to play the audio materials include cassette players
                                        (now almost obsolete), CD players and CD-ROMs (on computers and Laptops), MP3/MP4 players
                                        as well as digital players.
                                    </p>
                                    <p class="text-justify meme">
                                        For general maintenance of audio equipment, all tapes, tape recorders,
                                        CDs and CD players must be kept clean if they are to operate properly.
                                        While your school may not have all the equipment and gadgets to play audio
                                        materials, the rule of thumb is to do the following:

                                        <ul>
                                            <li>
                                                Keep them away from damp or moist places;
                                            </li>
                                            <li>
                                                Store them away from dust and direct sunshine;
                                            </li>
                                            <li>
                                                Use appropriate power sources; and
                                            </li>
                                            <li>
                                                Clean them regularly. Visit your local electronic shop for
                                                suggestions on proper cleaning materials of your equipment.
                                            </li>
                                        </ul>
                                    </p>
                                    <p class="text-justify meme">
                                        Parts which may require special cleaning solutions include player heads
                                        and or readers. As an example, to improve the quality of your recordings
                                        when you have cassettes, you should use a cassette head cleaner to clean
                                        the cassette recorder regularly. CD player heads should also be cleaned with
                                        special solutions. The CD's themselves must also be kept clean and refrain
                                        from scratching them. Let us now look at video equipment.
                                    </p>
                                </div>
