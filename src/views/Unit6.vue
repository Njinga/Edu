                                <h2 id="item-4" class="h2-responsive">6.4 Storing equipment and Materials</h2>
                                <div class="ml-5">
                                    <p class="text-justify meme">
                                        Storage of equipment helps you to use them longer
                                        and more efficiently without regular breakage.
                                        Therefore, an area should be created and partitioned
                                        where the equipment and materials can be stored. For convenience,
                                        shelves should be erected for storing the projectors and other equipment.
                                        Make sure that all equipment have an outer cover to protect them from dust.
                                        For good maintenance, essential spare parts such as projection lamps, belts,
                                        fuses, cables, etc., should always be made available for replacement when the old ones are broken.
                                    </p>

                                    <p class="text-justify meme">
                                        Materials like videos, CDs, DVDs, CD-ROMs, should be kept
                                        in racks built in cabinets and arranged with serial numbers
                                        for easy retrieval and quick reference.
                                    </p>
                                    <p class="text-justify meme">
                                        To store transparencies, use card boxes with cover and
                                        use a layer of paper in between the transparencies to
                                        prevent them from sticking against each other.
                                    </p>
                                    <p class="text-justify meme">
                                        For other materials like charts, posters, etc., store them
                                        in reasonably wide drawers and lay them flat to prevent them
                                        from being folded. Cut-outs from magazines and newspapers
                                        should be put in files or in large envelopes before storing them in drawers or cabinets.
                                    </p>
